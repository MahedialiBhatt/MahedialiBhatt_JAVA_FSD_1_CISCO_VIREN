package cisco.java.programs;

public class DataType
{
    public static void main(String[] args)
    {
        char gender='M';

        System.out.println(" value of gender is " + gender);

        int age=34;

        System.out.println(" value of age is " + age);

        long distance=127618747912L;

        System.out.println(" value of distance is " + distance);

        float pi=3.14567f;

        System.out.println(" value of pi is " + pi);

        double pii=3.14567;

        System.out.println(" value of pii is " + pii);

        byte b = 123;

        System.out.println(" value of byte is " + b);

        boolean v= true;

        System.out.println(" value of boolean is " + v);
    }
}
